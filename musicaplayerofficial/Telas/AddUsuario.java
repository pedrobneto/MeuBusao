    /**
     * Tela onde o usuario vip pode cadastrar um novo usuario
     * @param evt 
     */
    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        File log = new File("/home/cadoafb/informacoesUsuario.log");
        try {
            if (log.exists() == false) {
                log.createNewFile();
            }
            if (!novaS.getText().equals(confirmaS.getText())) {
                System.out.println("Suas senhas n√£o combinam.");
            } 
            else {
                PrintWriter out = new PrintWriter(new FileWriter(log, true));
                out.append(novoU.getText() + " " + novaS.getText() + " " + flagVIP + "\n");
                out.close();
                this.setVisible(false);
            }
        } catch (IOException e) {
            System.out.println("COULD NOT LOG!!");
        }
        
    }//GEN-LAST:event_cadastrarActionPerformed

