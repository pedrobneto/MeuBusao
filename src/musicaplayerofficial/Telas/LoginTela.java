    /**
     * Adiciona os usuarios do arquivo na árvore
     * depois busca o usuario digitado na arvore
     * @param evt 
     */
    private void entrarButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarButActionPerformed
        String _u = loginTexto.getText();
        String _s = senhaTexto.getText();
        
        try {
            adicionarUsuarios();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(LoginTela.class.getName()).log(Level.SEVERE, null, ex);
        }
        boolean ehVip = false;
        UsuarioVip tempUser = new UsuarioVip();
        tempUser.setUsuario(_u);
        tempUser.setSenha(_s);
        achouUsuario = bancoArvore.getInstance().getArvore().busca(tempUser) != null;
        
        if(achouUsuario && bancoArvore.getInstance().getArvore().busca(tempUser).getValue().isVip()) { 
            usuarioLogado = new UsuarioVip();
            usuarioLogado.setUsuario(tempUser.getUsuario());
            usuarioLogado.setSenha(tempUser.getSenha());
        }
        else if(achouUsuario){
            usuarioLogado = new UsuarioComum();
            usuarioLogado.setUsuario(tempUser.getUsuario());
            usuarioLogado.setSenha(tempUser.getSenha());
        }
        bancoArvore.getInstance().getArvore().imprimeEmOrdem();
        
        if(achouUsuario) {
            try {
                tela.repoeMusicasUsuario(usuarioLogado.getUsuario());
            } catch (FileNotFoundException ex) {
                System.out.println("Não possui playlists");
            }
                tela.setVisible(true);
        }
        else teErr.setVisible(true);

    }//GEN-LAST:event_entrarButActionPerformed

