    /**
     * Funcao reponsavel por adiconar os usuarios do arquivo na árvore binária
     * @throws FileNotFoundException 
     */
    private void adicionarUsuarios() throws FileNotFoundException {
        // Variáveis temporárias para armazenar os dados
        String currLogin = null;
        String currPass = null;
        boolean ehVip = false;
        
        // Cria o scanner separando por espaço
        File f = new File("/home/cadoafb/informacoesUsuario.log");
        Scanner fileScanner = new Scanner(f).useDelimiter("\\s");
        
        int lineNumber = 0;

        // Adiciona todos os usuários cadastrados à árvore
        while (fileScanner.hasNextLine()) {
            currLogin = fileScanner.next();
            currPass = fileScanner.next();
            ehVip = fileScanner.nextBoolean();
            
            // Se for VIP cria um UsuarioVip
            if (ehVip) {
                UsuarioBase u = new UsuarioVip();
                u.setUsuario(currLogin);
                u.setSenha(currPass);
                
                // Insere na árvore
                bancoArvore.getInstance().getArvore().insert(u);

              
                // Se não for cria um UsuarioComum
            } else if (!ehVip) {
                UsuarioBase u = new UsuarioComum();
                u.setUsuario(currLogin);
                u.setSenha(currPass);
                
                // Insere na árvore
                bancoArvore.getInstance().getArvore().insert(u);
            }
            lineNumber++;
            fileScanner.nextLine();
        }
        System.out.printf("%d Contas de Usuario(s)\n", lineNumber);
    }

